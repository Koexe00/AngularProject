export class Person {

public id: string;
public name: string;
public surname: string;
public age: number;
public description: string;
public image: string;


constructor(id: string, name: string, surname: string, age: number, description: string, image: string){
this.id = id;
this.name = name;
this.surname = surname;
this.age = age;
this.description = description;
this.image = image;
}

}
